// ── Context ───────────────────────────────────────────────────────────────────
const I18nCtx = createContext<{
  lang: LangId
  setLang: (l: LangId) => void
  t: Strings
}>({
  lang: 'en',
  setLang: () => {},
  t: STRINGS.en,
})

export function I18nProvider({ children }: { children: ReactNode }) {
  const stored = (localStorage.getItem('brandgeo_lang') ?? 'en') as LangId
  const valid  = LANGUAGES.some(l => l.id === stored) ? stored : 'en'
  const [lang, setLangState] = useState<LangId>(valid)

  function setLang(l: LangId) {
    localStorage.setItem('brandgeo_lang', l)
    setLangState(l)
  }

  return (
    <I18nCtx.Provider value={{ lang, setLang, t: STRINGS[lang] }}>
      {children}
    </I18nCtx.Provider>
  )
}

export function useI18n() {
  return useContext(I18nCtx)
}

// ── fmt helper ────────────────────────────────────────────────────────────────
export function fmt(template: string, vars: Record<string, string | number>) {
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replace(`{${k}}`, String(v)),
    template,
  )
}
